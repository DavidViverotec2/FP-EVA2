package eva2_9_for;
public class EVA2_9_FOR {
    public static void main(String[] args) {
        
        // 3 Parametros para funcionar
        // Punto inicial
        // Punto final
        // Como me voy a mover
        // ++ Sumar 1; -- Restar 1
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " Hola Mundo");
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println(i + " Hola Mundo");
        }
        
    }
    
}
