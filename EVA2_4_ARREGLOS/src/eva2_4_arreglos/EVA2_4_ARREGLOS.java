package eva2_4_arreglos;
public class EVA2_4_ARREGLOS {
    public static void main(String[] args) {
        // NECESITAN CAPTURAR LOS DATOS DE UN GRUPO
        // ARREGLO --> ESTRUCTURA DE DATOS
        // SECUANCIA (CONSECUTIVA) DE VALORES DEL MISMO TIPO
        
        int[] arreglo = new int[5]; //Crear el arreglo (decir su tamaño)
        // POSICIONES DE LOS LEMENTOS DE UN ARREGLO:
        // PRIMER ELEMENTO --> 0
        // ULTIMO ELEMENTO --> TAMAÑO - 1
        arreglo[0] = 100;
        arreglo[1] = 200;
        arreglo[2] = 300;
        arreglo[3] = 400;
        arreglo[4] = 500;
        
        System.out.println(arreglo[0]);
        System.out.println(arreglo[1]);
        System.out.println(arreglo[2]);
        System.out.println(arreglo[3]);
        System.out.println(arreglo[4]);
    }
    
}
